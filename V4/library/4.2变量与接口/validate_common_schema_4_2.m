function report = validate_common_schema_4_2(cfg, strictMode)
%VALIDATE_COMMON_SCHEMA_4_2 Validate the Chapter 4 common configuration.
%
% report = validate_common_schema_4_2(cfg,false) returns all errors and
% warnings without throwing. strictMode=true throws if any error exists.

if nargin < 2
    strictMode = true;
end
assert(isstruct(cfg) && isscalar(cfg),'cfg must be a scalar struct.');
assert(isscalar(strictMode),'strictMode must be scalar.');

errors = {};
warnings = {};

requiredPaths = { ...
    'meta.schemaId','meta.schemaVersion','meta.schemaFrozen','meta.caseId', ...
    'meta.caseMode','meta.parameterVersion','meta.status', ...
    'contract.schemaAuthority','contract.boundaryAuthority', ...
    'contract.dispatchOwner','contract.actualBalanceOwner', ...
    'contract.noSilentDefaultsInIntegratedRun','contract.actualOnlyInBusLedger', ...
    'time.sourceRawStepH','time.dispatchStepH','time.intradayCheckStepH', ...
    'time.dynamicMaxStepS','units.power','units.energy','units.mass', ...
    'meter.sourcePOI','meter.commonBus','meter.cableSend','meter.cableReceive', ...
    'source.windCapacityMW','source.pvCapacityMW','source.tidalCapacityMW', ...
    'source.totalCapacityMW','commonBus.balanceToleranceMW', ...
    'bess.powerMW','bess.energyMWh','bess.chargeMaxMW','bess.dischargeMaxMW', ...
    'bess.etaCharge','bess.etaDischarge','bess.socMin','bess.socMax','bess.socInitial', ...
    'hydrogen.electrolyzerRatedMW','hydrogen.electrolyzerMinMW', ...
    'hydrogen.secKWhPerKg','hydrogen.storageMaxKg','hydrogen.storageInitialKg', ...
    'compute.facilityMaxMW','compute.facilityMinMW','compute.itCapacityMW', ...
    'compute.pueMin','compute.fiberServiceCapacityMWhCSPerH', ...
    'output.cableSendCapacityMW','output.gridAcceptMaxMW', ...
    'output.cableLossFraction','economics.currencyBaseYear', ...
    'economics.analysisLifeYear','scenario.randomSeed','p0.projectTeamSigned', ...
    'interface.commonMeta','interface.commonAxis','interface.commonQuality', ...
    'interface.commonDispatchPort','interface.sourceToBus','interface.bessToBus', ...
    'interface.hydrogenToBus','interface.computeToBus','interface.outputToBus', ...
    'interface.busLedger'};

for i = 1:numel(requiredPaths)
    [exists,value] = get_path(cfg,requiredPaths{i});
    if ~exists
        errors{end+1} = ['Missing required field: ' requiredPaths{i}]; %#ok<AGROW>
    elseif isnumeric(value) && isscalar(value) && isnan(value)
        errors{end+1} = ['Unresolved P0 value: ' requiredPaths{i}]; %#ok<AGROW>
    elseif isempty(value)
        errors{end+1} = ['Empty required field: ' requiredPaths{i}]; %#ok<AGROW>
    end
end

errors = add_check(errors,strcmp(cfg.meta.schemaId,'BLUE_HUB_CH4_SCHEMA_V2'), ...
    'meta.schemaId is not BLUE_HUB_CH4_SCHEMA_V2.');
errors = add_check(errors,strcmp(cfg.meta.schemaVersion,'2.0.0'), ...
    'meta.schemaVersion is not the frozen version 2.0.0.');
errors = add_check(errors,isequal(cfg.meta.schemaFrozen,true), ...
    'meta.schemaFrozen must remain true.');
errors = add_check(errors,strcmp(cfg.meta.status,'FROZEN_INTERFACE'), ...
    'meta.status must remain FROZEN_INTERFACE.');
errors = add_check(errors,strcmp(cfg.meta.caseId,'common_case_v2'), ...
    'meta.caseId is not common_case_v2.');
errors = add_check(errors,any(strcmp(cfg.meta.caseMode, ...
    {'interface_smoke','engineering_base'})), 'Unsupported caseMode.');
errors = add_check(errors,strcmp(cfg.contract.schemaAuthority,'4.2'), ...
    'Schema authority must be 4.2.');
errors = add_check(errors,strcmp(cfg.contract.boundaryAuthority,'4.1'), ...
    'Boundary authority must be 4.1.');
errors = add_check(errors,strcmp(cfg.contract.dispatchOwner,'4.9'), ...
    'Cross-module requested values must be owned by 4.9.');
errors = add_check(errors,strcmp(cfg.contract.actualBalanceOwner,'4.4'), ...
    'The actual bus ledger must be owned by 4.4.');
errors = add_check(errors,isequal(cfg.contract.noSilentDefaultsInIntegratedRun,true), ...
    'Integrated runs must prohibit silent module defaults.');
errors = add_check(errors,isequal(cfg.contract.noCrossModuleStateMutation,true), ...
    'Cross-module state mutation must be prohibited.');
errors = add_check(errors,isequal(cfg.contract.actualOnlyInBusLedger,true), ...
    '4.4 must use actual values only.');
errors = add_check(errors,isequal(cfg.contract.requestedAcceptedActualRequired,true), ...
    'Every controllable electrical port must retain requested/accepted/actual.');

errors = add_check(errors,strcmp(cfg.units.power,'MW') && ...
    strcmp(cfg.units.reactivePower,'Mvar') && strcmp(cfg.units.energy,'MWh') && ...
    strcmp(cfg.units.time,'h') && strcmp(cfg.units.mass,'kg'), ...
    'Frozen optimization-layer units have changed.');

errors = add_check(errors,isfinite_positive(cfg.time.sourceRawStepH), ...
    'sourceRawStepH must be positive.');
errors = add_check(errors,isfinite_positive(cfg.time.dispatchStepH), ...
    'dispatchStepH must be positive.');
errors = add_check(errors,isfinite_positive(cfg.time.intradayCheckStepH), ...
    'intradayCheckStepH must be positive.');
errors = add_check(errors,isfinite_positive(cfg.time.dynamicMaxStepS), ...
    'dynamicMaxStepS must be positive.');
if isfinite_positive(cfg.time.sourceRawStepH) && isfinite_positive(cfg.time.dispatchStepH)
    ratio = cfg.time.dispatchStepH/cfg.time.sourceRawStepH;
    errors = add_check(errors,abs(ratio-round(ratio))<1e-10, ...
        'dispatchStepH must be an integer multiple of sourceRawStepH.');
end
if isfinite_positive(cfg.time.intradayCheckStepH) && isfinite_positive(cfg.time.dispatchStepH)
    ratio = cfg.time.dispatchStepH/cfg.time.intradayCheckStepH;
    errors = add_check(errors,abs(ratio-round(ratio))<1e-10, ...
        'dispatchStepH must be an integer multiple of intradayCheckStepH.');
end

sourceValues = [cfg.source.windCapacityMW cfg.source.pvCapacityMW ...
    cfg.source.tidalCapacityMW cfg.source.totalCapacityMW];
if all(isfinite(sourceValues))
    errors = add_check(errors,all(sourceValues>=0), ...
        'Source capacities must be nonnegative.');
    errors = add_check(errors,abs(sum(sourceValues(1:3))-sourceValues(4))<=1e-9, ...
        'source.totalCapacityMW does not equal wind+PV+tidal.');
end

errors = check_range(errors,cfg.bess.etaCharge,0,1,'bess.etaCharge');
errors = check_range(errors,cfg.bess.etaDischarge,0,1,'bess.etaDischarge');
errors = check_range(errors,cfg.bess.socMin,0,1,'bess.socMin');
errors = check_range(errors,cfg.bess.socMax,0,1,'bess.socMax');
errors = check_range(errors,cfg.bess.socInitial,0,1,'bess.socInitial');
if all(isfinite([cfg.bess.socMin cfg.bess.socInitial cfg.bess.socMax]))
    errors = add_check(errors,cfg.bess.socMin<=cfg.bess.socInitial && ...
        cfg.bess.socInitial<=cfg.bess.socMax, ...
        'BESS SOC must satisfy socMin <= socInitial <= socMax.');
end
if all(isfinite([cfg.bess.powerMW cfg.bess.chargeMaxMW cfg.bess.dischargeMaxMW]))
    errors = add_check(errors,cfg.bess.chargeMaxMW<=cfg.bess.powerMW && ...
        cfg.bess.dischargeMaxMW<=cfg.bess.powerMW, ...
        'BESS charge/discharge limit exceeds bess.powerMW.');
end

errors = check_nonnegative(errors,cfg.hydrogen.electrolyzerRatedMW, ...
    'hydrogen.electrolyzerRatedMW');
errors = check_nonnegative(errors,cfg.hydrogen.electrolyzerMinMW, ...
    'hydrogen.electrolyzerMinMW');
if all(isfinite([cfg.hydrogen.electrolyzerRatedMW cfg.hydrogen.electrolyzerMinMW]))
    errors = add_check(errors,cfg.hydrogen.electrolyzerMinMW<= ...
        cfg.hydrogen.electrolyzerRatedMW, ...
        'Electrolyzer minimum exceeds rated power.');
end
errors = check_nonnegative(errors,cfg.hydrogen.secKWhPerKg, ...
    'hydrogen.secKWhPerKg');
if all(isfinite([cfg.hydrogen.storageInitialKg cfg.hydrogen.storageMaxKg]))
    errors = add_check(errors,cfg.hydrogen.storageInitialKg>=0 && ...
        cfg.hydrogen.storageInitialKg<=cfg.hydrogen.storageMaxKg, ...
        'Hydrogen initial inventory is outside storage bounds.');
end

errors = check_range(errors,cfg.output.cableLossFraction,0,1, ...
    'output.cableLossFraction');
errors = check_range(errors,cfg.output.marineFlexibleFraction,0,1, ...
    'output.marineFlexibleFraction');
if all(isfinite([cfg.compute.facilityMinMW cfg.compute.facilityMaxMW]))
    errors = add_check(errors,cfg.compute.facilityMinMW<=cfg.compute.facilityMaxMW, ...
        'Compute minimum facility power exceeds maximum.');
end
if isfinite(cfg.compute.pueMin)
    errors = add_check(errors,cfg.compute.pueMin>=1, ...
        'compute.pueMin must be at least 1.');
end

errors = add_check(errors,strcmp(cfg.source.collectionLossOwner,'4.3'), ...
    'Collection loss owner must be 4.3.');
errors = add_check(errors,strcmp(cfg.source.auxiliaryCalculationOwner,'4.3'), ...
    'Source auxiliary calculation owner must be 4.3.');
errors = add_check(errors,strcmp(cfg.source.auxiliaryAccountingOwner,'4.4'), ...
    'Source auxiliary accounting owner must be 4.4.');
errors = add_check(errors,strcmp(cfg.commonBus.balanceOwner,'4.4'), ...
    'The only common-bus balance owner must be 4.4.');
errors = add_check(errors,strcmp(cfg.hydrogen.ownerInternal,'4.5'), ...
    'Hydrogen internal-state owner must be 4.5.');
errors = add_check(errors,strcmp(cfg.compute.ownerInternal,'4.6'), ...
    'Compute internal-state owner must be 4.6.');
errors = add_check(errors,strcmp(cfg.output.owner,'4.7'), ...
    'Delivery-channel owner must be 4.7.');
errors = add_check(errors,strcmp(cfg.commonBus.spillDecisionOwner,'4.9') && ...
    strcmp(cfg.commonBus.unservedDecisionOwner,'4.9'), ...
    'Spill and unserved decisions must be owned by 4.9.');
errors = add_check(errors,strcmp(cfg.commonBus.residualCalculationOwner,'4.4'), ...
    'Power residual calculation must be owned by 4.4.');
errors = add_check(errors,isequal(cfg.commonBus.implicitClosureAllowed,false), ...
    '4.4 must not silently close a residual with implicit spill/unserved power.');
errors = add_check(errors,strcmp(cfg.hydrogen.inventoryCommitOwner,'4.5'), ...
    'Hydrogen inventory commit owner must be 4.5.');
errors = add_check(errors,strcmp(cfg.compute.queueCommitOwner,'4.6'), ...
    'Compute queue commit owner must be 4.6.');

requiredMeters = {cfg.meter.sourcePOI,cfg.meter.commonBus,cfg.meter.cableSend, ...
    cfg.meter.cableReceive,cfg.meter.dcFacility,cfg.meter.electrolyzer, ...
    cfg.meter.hydrogenDelivery,cfg.meter.computeDelivery};
errors = add_check(errors,numel(unique(requiredMeters))==numel(requiredMeters), ...
    'Meter-point identifiers must be unique.');

expectedMeta = {'schemaId','schemaVersion','caseId','caseMode', ...
    'parameterVersion','moduleId','phase'};
expectedAxis = {'timeH','dtH','scenarioId','scenarioProbability'};
expectedDispatch = {'requestedMW','acceptedMW','actualMW','minMW','maxMW', ...
    'upCapabilityMW','downCapabilityMW','constraintCode','operatingState', ...
    'meterPoint','qualityFlag'};
errors = add_check(errors,isequal(cfg.interface.commonMeta,expectedMeta), ...
    'interface.commonMeta differs from the frozen field order.');
errors = add_check(errors,isequal(cfg.interface.commonAxis,expectedAxis), ...
    'interface.commonAxis differs from the frozen field order.');
errors = add_check(errors,isequal(cfg.interface.commonDispatchPort,expectedDispatch), ...
    'interface.commonDispatchPort differs from the frozen field order.');

if strcmp(cfg.meta.caseMode,'engineering_base')
    if ~cfg.p0.projectTeamSigned
        errors{end+1} = 'engineering_base is not signed in 4.1 (p0.projectTeamSigned=false).';
    end
    if cfg.site.isVirtualReferenceSite
        errors{end+1} = 'engineering_base cannot use a virtual reference site.';
    end
else
    warnings{end+1} = ['interface_smoke values are assumptions and cannot support ' ...
        'engineering, economic or LCA conclusions.'];
end

if strcmp(cfg.economics.taxBasis,'UNDECIDED')
    warnings{end+1} = 'Economic tax basis is undecided.';
end
if strcmp(cfg.economics.sharedAssetAllocationMethod,'UNDECIDED')
    warnings{end+1} = 'Shared-asset allocation method is undecided.';
end
if strcmp(cfg.lca.allocationMethod,'UNDECIDED')
    warnings{end+1} = 'LCA allocation method is undecided.';
end

report = struct;
report.schemaId = cfg.meta.schemaId;
report.caseId = cfg.meta.caseId;
report.caseMode = cfg.meta.caseMode;
report.ok = isempty(errors);
report.errorCount = numel(errors);
report.warningCount = numel(warnings);
report.errors = errors(:);
report.warnings = warnings(:);
report.checkedAt = datestr(now,30);

if strictMode && ~report.ok
    error('BLUEHUB:CommonSchemaInvalid','%s',strjoin(errors,newline));
end
end

function errors = add_check(errors,condition,message)
if ~condition
    errors{end+1} = message;
end
end

function errors = check_range(errors,value,lower,upper,name)
if isfinite(value)
    errors = add_check(errors,value>=lower && value<=upper, ...
        [name ' must lie in [' num2str(lower) ',' num2str(upper) '].']);
end
end

function errors = check_nonnegative(errors,value,name)
if isfinite(value)
    errors = add_check(errors,value>=0,[name ' must be nonnegative.']);
end
end

function tf = isfinite_positive(value)
tf = isnumeric(value) && isscalar(value) && isfinite(value) && value>0;
end

function [exists,value] = get_path(s,path)
parts = strsplit(path,'.');
value = s;
exists = true;
for i = 1:numel(parts)
    if ~isstruct(value) || ~isfield(value,parts{i})
        exists = false;
        value = [];
        return;
    end
    value = value.(parts{i});
end
end
