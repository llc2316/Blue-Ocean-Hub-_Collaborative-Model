function packet = common_packet_4_2(moduleId,timeH,cfg,phase)
%COMMON_PACKET_4_2 Create a frozen-schema Chapter 4 exchange packet.
% This factory is the only supported starting point for 4.3--4.7 adapters.

if nargin < 3 || isempty(cfg)
    error('BLUEHUB:ConfigRequired', ...
        'Integrated packets require an explicit validated common cfg.');
end
validate_common_schema_4_2(cfg,true);
moduleId = validatestring(char(moduleId),{'4.3','4.4','4.5','4.6','4.7','4.8','4.9'});
if nargin<4 || isempty(phase), phase='BOUNDARY'; end
phase=validatestring(char(phase),cfg.enums.packetPhase);
timeH = timeH(:);
assert(~isempty(timeH) && all(isfinite(timeH)) && ...
    (numel(timeH)==1 || all(diff(timeH)>0)), ...
    'timeH must be finite and strictly increasing.');

N = numel(timeH);
packet = struct;
packet.meta = struct( ...
    'schemaId',cfg.meta.schemaId, ...
    'schemaVersion',cfg.meta.schemaVersion, ...
    'caseId',cfg.meta.caseId, ...
    'caseMode',cfg.meta.caseMode, ...
    'parameterVersion',cfg.meta.parameterVersion, ...
    'moduleId',moduleId, ...
    'phase',phase);
packet.axis = struct( ...
    'timeH',timeH, ...
    'dtH',cfg.time.dispatchStepH, ...
    'scenarioId',"BASE", ...
    'scenarioProbability',1.0);
packet.ports = struct;
packet.state = struct;
packet.product = struct;
packet.service = struct;
packet.loss = struct;
packet.quality = struct( ...
    'qualityFlag',true(N,1), ...
    'dataSourceType',"UNSPECIFIED", ...
    'calibrationVersion',"UNSPECIFIED");
packet.audit = struct( ...
    'assumptionNotice',cfg.meta.parameterNotice, ...
    'createdBy','common_packet_4_2', ...
    'implicitDefaultsUsed',false);
end
