function port = common_dispatch_port_4_2(timeH,meterPoint)
%COMMON_DISPATCH_PORT_4_2 Create one nonnegative electrical port contract.

timeH=timeH(:); N=numel(timeH);
assert(N>=1 && all(isfinite(timeH)) && (N==1 || all(diff(timeH)>0)), ...
    'timeH must be finite and strictly increasing.');
assert(ischar(meterPoint) || (isstring(meterPoint) && isscalar(meterPoint)), ...
    'meterPoint must be scalar text.');
nanSeries=nan(N,1);
port=struct( ...
    'requestedMW',nanSeries, ...
    'acceptedMW',nanSeries, ...
    'actualMW',nanSeries, ...
    'minMW',zeros(N,1), ...
    'maxMW',nanSeries, ...
    'upCapabilityMW',nanSeries, ...
    'downCapabilityMW',nanSeries, ...
    'constraintCode',repmat("NONE",N,1), ...
    'operatingState',repmat("AVAILABLE",N,1), ...
    'meterPoint',string(meterPoint), ...
    'qualityFlag',true(N,1));
end

